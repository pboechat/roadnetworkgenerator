#include <Globals.h>

//////////////////////////////////////////////////////////////////////////
Configuration* configuration = 0;
//////////////////////////////////////////////////////////////////////////
RoadNetworkGraph::Graph* graph = 0;
//////////////////////////////////////////////////////////////////////////
unsigned char* populationDensitiesSamplingBuffer = 0;
//////////////////////////////////////////////////////////////////////////
unsigned int* distancesSamplingBuffer = 0;

//////////////////////////////////////////////////////////////////////////
void initializeSamplingBuffers()
{
	populationDensitiesSamplingBuffer = new unsigned char[configuration->samplingArc];
	distancesSamplingBuffer = new unsigned int[configuration->samplingArc];
}

//////////////////////////////////////////////////////////////////////////
void disposeSamplingBuffers()
{
	if (populationDensitiesSamplingBuffer != 0)
	{
		delete[] populationDensitiesSamplingBuffer;
	}

	if (distancesSamplingBuffer != 0)
	{
		delete[] distancesSamplingBuffer;
	}
}